package com.greatlearning.main;

public class Building {
	
	int floorSize;
	
	int pendingFloors;

	/**
	 * @return the floorSize
	 */
	public int getFloorSize() {
		return floorSize;
	}

	/**
	 * @param floorSize the floorSize to set
	 */
	public void setFloorSize(int floorSize) {
		this.floorSize = floorSize;
	}

	/**
	 * @return the pendingFloors
	 */
	public int getPendingFloors() {
		return pendingFloors;
	}

	/**
	 * @param pendingFloors the pendingFloors to set
	 */
	public void setPendingFloors(int pendingFloors) {
		this.pendingFloors = pendingFloors;
	}

	

}
